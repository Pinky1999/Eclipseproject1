package javapractice;

public class For_loop {

	public static void main(String[] args) {
		
		for(int i=1;i<=10;i++)
		{
			System.out.println(i);
		}
		
		for( int j=1;j<=5;j++)
		{
			System.out.println("Pinky");
		}
		
		for(char c='A';c<='Z';c++)
		{
			System.out.println(c);
		}
		
		for(int d=20;d<=70;d++)
		{
			System.out.println(d);
		}

	}
	
}
		
	
